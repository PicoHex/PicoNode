using System.Net;
using PicoDI;
using PicoDI.Abs;
using PicoNode.Web;
using PicoWeb;

// ── DI First WebAPI with Controller ─────────────────────
//
// Three ways to register endpoints:
// 1. MapXX — inline handlers
// 2. Controller in Controllers/ folder — auto-discovered by Controllers.Gen
// 3. HttpGet/HttpPost attributes

var container = new SvcContainer();
container.Register(new SvcDescriptor(
    serviceType: typeof(PicoWeb.Samples.Controllers.UsersController),
    implementationType: typeof(PicoWeb.Samples.Controllers.UsersController),
    lifetime: PicoDI.Abs.SvcLifetime.Scoped));
var app = new WebApp(container);

// Register controller endpoints (auto-generated by Controllers.Gen)
EndpointRegistrar.RegisterAll(app);

// MapXX endpoint — return pre-serialized JSON to avoid PicoJetson.Gen anonymous type issue
app.MapGet("/api/health", (WebContext ctx) => Results.Json(200, """{"status":"ok"}"""u8.ToArray()));

// Start
var server = new WebServer(
    app,
    new WebServerOptions { Endpoint = new IPEndPoint(IPAddress.Loopback, 7004) }
);
await server.StartAsync();
Console.WriteLine($"Listening on {server.LocalEndPoint}");
Console.ReadLine();
await server.DisposeAsync();
